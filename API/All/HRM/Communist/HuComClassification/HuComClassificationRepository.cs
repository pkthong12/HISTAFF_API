        public async Task<GenericPhaseTwoListResponse<HuComClassificationDTO>> SinglePhaseQueryList(GenericQueryListDTO<HuComClassificationDTO> request)
        {
            var joined = from p in _dbContext.HuComClassifications.AsNoTracking()
                         // JOIN OTHER ENTITIES BASED ON THE BUSINESS
                         from e in _dbContext.HuEmployees.AsNoTracking().Where(x => x.ID == p.EMPLOYEE_ID).DefaultIfEmpty()
                         from o in _dbContext.HuOrganizations.AsNoTracking().Where(x => x.ID == e.ID).DefaultIfEmpty()
                         select new HuComClassificationDTO
                         {
                             Id = p.ID,
                             OrgId = e.ORG_ID
                         };

            var singlePhaseResult = await _genericReducer.SinglePhaseReduce(joined, request);
            return singlePhaseResult;
        }

