        private readonly GenericUnitOfWork _uow;
        private readonly FullDbContext _dbContext;
        private IGenericRepository<SYS_ACTION, SysActionDTO> _genericRepository;
        private readonly GenericReducer<SYS_ACTION, SysActionDTO> _genericReducer;

        public SysActionRepository(FullDbContext context, GenericUnitOfWork uow)
        {
            _dbContext = context;
            _uow = uow;
            _genericRepository = _uow.GenericRepository<SYS_ACTION, SysActionDTO>();
            _genericReducer = new();
        }

        public async Task<GenericPhaseTwoListResponse<SysActionDTO>> SinglePhaseQueryList(GenericQueryListDTO<SysActionDTO> request)
        {
            var joined = from p in _dbContext.SysActions.AsNoTracking()
                             // JOIN OTHER ENTITIES BASED ON THE BUSINESS
                         select new SysActionDTO
                         {
                             Id = p.ID,
                             Code = p.CODE
                         };

            var singlePhaseResult = await _genericReducer.SinglePhaseReduce(joined, request);
            return singlePhaseResult;
        }

        public async Task<FormatedResponse> ReadAll()
        {
            var response = await _genericRepository.ReadAll();
            return response;
        }
        public async Task<FormatedResponse> GetAll()
        {
            try
            {
                var query = await (from p in _dbContext.SysActions
                                   select new
                                   {
                                       Id = p.ID,
                                       Code = p.CODE
                                   }).ToListAsync();
                return (new FormatedResponse()
                {
                    InnerBody = query
                });
            }
            catch (Exception ex)
            {
                return (new FormatedResponse() { ErrorType = EnumErrorType.UNCATCHABLE, StatusCode = EnumStatusCode.StatusCode500, MessageCode = ex.Message });
            }
        }

        public async Task<FormatedResponse> ReadAllByKey(string key, long value)
        {
            var resposne = await _genericRepository.ReadAllByKey(key, value);
            return new() { InnerBody = resposne };
        }

        public async Task<FormatedResponse> ReadAllByKey(string key, string value)
