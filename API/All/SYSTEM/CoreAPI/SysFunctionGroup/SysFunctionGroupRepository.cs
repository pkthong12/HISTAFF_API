        public async Task<GenericPhaseTwoListResponse<SysFunctionGroupDTO>> SinglePhaseQueryList(GenericQueryListDTO<SysFunctionGroupDTO> request)
        {
            var joined = from p in _dbContext.SysFunctionGroups.AsNoTracking()
                             // JOIN OTHER ENTITIES BASED ON THE BUSINESS
                         select new SysFunctionGroupDTO
                         {
                             Id = p.ID,
                             Code = p.CODE,
                             Name = p.NAME
                         };

            var singlePhaseResult = await _genericReducer.SinglePhaseReduce(joined, request);
            return singlePhaseResult;
        }

